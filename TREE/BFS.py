class TreeNode(object):
    def __init__(self, x):
        self.val = x
        self.left = None
        self.right = None

class Solution:
    def levelOrder(self, root: TreeNode):
        res = []
        if not root:
            return res

        q = []
        q.append(root)
        # while å¾ªç¯æ§åˆ¶ä»ä¸Šå‘ä¸‹ä¸€å±‚å±‚éå†
        while q:
            sz = len(q)
            # è®°å½•è¿™ä¸€å±‚çš„èŠ‚ç‚¹å€¼
            level = []
            # for å¾ªç¯æ§åˆ¶æ¯ä¸€å±‚ä»å·¦å‘å³éå†
            for i in range(sz):
                cur = q.pop(0)
                level.append(cur.val)
                if cur.left:
                    q.append(cur.left)
                if cur.right:
                    q.append(cur.right)
            res.append(level)
        return res